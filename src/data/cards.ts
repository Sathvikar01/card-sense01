export function getCardById(id: string): CreditCard | undefined {
  return cards.find((c) => c.id === id);
}

export function getCardsForCategories(
  categories: string[]
): CreditCard[] {
  return cards
    .filter((card) => card.bestFor.some((cat) => categories.includes(cat)))
    .sort((a, b) => {
      const aMatch = a.bestFor.filter((cat) => categories.includes(cat)).length;
      const bMatch = b.bestFor.filter((cat) => categories.includes(cat)).length;
      return bMatch - aMatch;
    });
}
